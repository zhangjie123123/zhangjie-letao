//入口函数
$(function () {
$("#form").bootstrapValidator({
    //配置字段
    fields:{
        username:{
            validators:{
                //非空
                notEmpty:{
                    message:"用户名不能为空"
                },
                stringLength:{
                    min:2,
                    max:6,
                    message:"用户名在2-6位数"
                }
            }
        },
        password:{
            validators:{
                notEmpty:{
                    message:"密码不能为空"
                },
                stringLength:{
                    min:6,
                    max:12,
                    message:"密码长度必须是6-12位数"
                }
            }
        }
    }
});
});